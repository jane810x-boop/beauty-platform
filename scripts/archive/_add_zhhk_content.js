function toHK(text) {
  let result = text;
  const keys = Object.keys(zhToHK).sort((a, b) => b.length - a.length);
  for (const zh of keys) {
    if (result.includes(zh)) {
      result = result.split(zh).join(zhToHK[zh]);
    }
  }
  return result;
}

// Find zh: { ... } blocks and add zh-HK: { ... } after
// Pattern: zh: { (title, excerpt, body) }

// Use a parser approach: find zh: { then count braces
let newContent = content;
let inserts = 0;
let offset = 0;

// Find all zh: { positions
const zhBlockStarts = [];
const regex = /\bzh:\s*\{/g;
let match;
while ((match = regex.exec(content)) !== null) {
  // Check if next chars look like title/excerpt/body
  const afterZh = content.substring(match.index, match.index + 100);
  if (afterZh.includes('title:') || afterZh.includes('excerpt:') || afterZh.includes('body:')) {
    zhBlockStarts.push(match.index);
  }
}

console.log(`Found ${zhBlockStarts.length} zh content blocks`);

for (const startIdx of zhBlockStarts.sort((a, b) => a - b)) {
  // Find matching closing brace
  let braceCount = 0;
  let endIdx = startIdx;
  let inZhBlock = false;

  for (let i = startIdx; i < content.length; i++) {
    if (content[i] === '{') {
      braceCount++;
      inZhBlock = true;
    } else if (content[i] === '}') {
      braceCount--;
      if (braceCount === 0 && inZhBlock) {
        endIdx = i;
        break;
      }
    }
  }

  // Extract zh block
  const zhBlock = content.substring(startIdx, endIdx + 1);

  // Check if already has zh-HK after it
  const afterBlock = content.substring(endIdx + 1, endIdx + 20);
  if (afterBlock.includes("'zh-HK'") || afterBlock.includes('zh-HK')) {
    console.log('  Skipping (already has zh-HK)');
    continue;
  }

  // Convert zh block to zh-HK
  let hkBlock = zhBlock.replace(/\bzh:/g, "'zh-HK':");

  // Convert all string values
  hkBlock = hkBlock.replace(/'([^']+)'/g, (match, p1) => {
    if (/[\u4e00-\u9fff]/.test(p1)) {
      return "'" + toHK(p1) + "'";
    }
    return match;
  });

  // Also convert template strings
  hkBlock = hkBlock.replace(/`([^`]+)`/g, (match, p1) => {
    if (/[\u4e00-\u9fff]/.test(p1)) {
      return "`" + toHK(p1) + "`";
    }
    return match;
  });

  // Insert after zh block
  const insertPos = endIdx + 1 + offset;
  newContent = newContent.substring(0, insertPos) + ',\n      ' + hkBlock + newContent.substring(insertPos);
  offset += 2 + hkBlock.length;
  inserts++;
}

console.log(`\nInserted ${inserts} zh-HK content blocks`);

if (inserts > 0) {
  fs.writeFileSync(blogPath, newContent, 'utf8');
  console.log('File updated');
}
