def convert_s2t(text):
    result = []
    for ch in text:
        result.append(S2T.get(ch, ch))
    return ''.join(result)

with open(FILE, 'r', encoding='utf-8') as f:
    content = f.read()

# Find the zhHK: block (from "zhHK: {" to its matching closing "}")
idx = content.find('zhHK: {')
if idx == -1:
    print("zhHK block not found!")
    exit(1)

start = idx + len('zhHK: {')
# Find matching closing brace
depth = 1
end = start
while end < len(content) and depth > 0:
    if content[end] == '{':
        depth += 1
    elif content[end] == '}':
        depth -= 1
    end += 1

block = content[start:end-1]
converted = convert_s2t(block)
result = content[:start] + converted + content[end-1:]

with open(FILE, 'w', encoding='utf-8') as f:
    f.write(result)

print(f"Converted zhHK block in i18n.js ({len(block)} chars -> {len(converted)} chars)")
